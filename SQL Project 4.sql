---23.Give the total weekly sales value with the date and product IDdetails. Use
      ---roll-up to pull the data in hierarchical order

	  SELECT
	  DATEADD(WEEK, DATEDIFF(WEEK, 0, [Date]), 0) AS Date,
	  ProductID,
	  SUM(Sales) as totals
	  FROM
	  fact
	  GROUP BY ROLLUP
	  (
	     DATEADD(WEEK, DATEDIFF(WEEK, 0, [Date]), 0),
	     ProductId
	  );
GO

